#include "header.h"

int		ft_dim(t_list **chain)
{
	int		i;
	t_list	*tmp;

	i = 0;
	tmp = *chain;
	while (tmp->next)
	{
		if (tmp->data == '\n')
			i++;
		tmp = tmp->next;
	}
	return (i);
}
